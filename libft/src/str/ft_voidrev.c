#include "ft_str.h"

void	*ft_voidrev(void *s)
{
	size_t		i;
	size_t		j;
	char		*c;

	i = 0;
	c = (char *)s;
	while (c[i])
		i++;
	j = 0;
	i--;
	while (i > j)
	{
		ft_voidswap(&c[i--], &c[j++]);
	}
	return (s);
}
